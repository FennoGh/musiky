#!/usr/bin/env bash
# Detect this laptop's current LAN IP, rewire every place that has it baked in
# (Next env, Capacitor config, Android cleartext whitelist), restart the dev
# servers bound on 0.0.0.0, then rebuild + reinstall + launch the Android app
# on the connected device.
#
# Run this every time the laptop joins a different network (Wi-Fi, hotspot,
# Ethernet). The phone must be on the same network when you launch the app.

set -euo pipefail

ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
cd "$ROOT"

# 1) Detect current LAN IP (the one used to reach the internet, so it picks
#    Wi-Fi vs hotspot vs Ethernet automatically).
IP="$(ip -4 route get 1.1.1.1 2>/dev/null | awk '/src/ {print $7; exit}')"
if [[ -z "${IP:-}" ]]; then
    echo "ERROR: could not detect a LAN IP. Are you online?" >&2
    exit 1
fi
echo "==> Detected LAN IP: $IP"

# Skip the slow APK rebuild when nothing about the bundled IP changed AND a
# previous APK is already installed. Pass FORCE=1 to override (e.g. after
# editing icons or other resources).
LAST_IP_FILE=".dev-mobile-last-ip"
LAST_IP="$(cat "$LAST_IP_FILE" 2>/dev/null || true)"
SKIP_BUILD=0
if [[ "$LAST_IP" == "$IP" && -z "${FORCE:-}" && -f "android/app/build/outputs/apk/debug/app-debug.apk" ]]; then
    SKIP_BUILD=1
    echo "==> IP unchanged since last build — will only restart servers."
    echo "    (Pass FORCE=1 to force a full rebuild + reinstall.)"
fi

# 2) Frontend env (read at Next startup; baked into the JS bundle).
cat > .env.local <<EOF
NEXT_PUBLIC_API_BASE=http://${IP}:8000
EOF
echo "==> Wrote .env.local with NEXT_PUBLIC_API_BASE=http://${IP}:8000"

# 3) Capacitor config (read at `cap sync`; baked into the APK).
cat > capacitor.config.ts <<EOF
import type { CapacitorConfig } from "@capacitor/cli";

// Auto-generated by bin/dev-mobile.sh — do not edit by hand. Re-run the script
// after switching networks to update the URL.
const config: CapacitorConfig = {
  appId: "dev.musiky.app",
  appName: "Musiky",
  webDir: "public",
  // Match the WebView scheme to the actual URL so a clear-data wipe doesn't
  // leave behind an https://localhost/ origin in the navigation history.
  server: {
    url: "http://${IP}:3000",
    cleartext: true,
    androidScheme: "http",
  },
  android: {
    allowMixedContent: true,
    webContentsDebuggingEnabled: true,
  },
};

export default config;
EOF
echo "==> Wrote capacitor.config.ts with server.url=http://${IP}:3000"

# 4) Android cleartext whitelist (read at gradle build; baked into the APK).
mkdir -p android/app/src/main/res/xml
cat > android/app/src/main/res/xml/network_security_config.xml <<EOF
<?xml version="1.0" encoding="utf-8"?>
<network-security-config>
    <base-config cleartextTrafficPermitted="true">
        <trust-anchors>
            <certificates src="system" />
        </trust-anchors>
    </base-config>
    <domain-config cleartextTrafficPermitted="true">
        <domain includeSubdomains="false">${IP}</domain>
        <domain includeSubdomains="false">localhost</domain>
        <domain includeSubdomains="false">10.0.2.2</domain>
    </domain-config>
</network-security-config>
EOF
echo "==> Updated network_security_config.xml for ${IP}"

# 5) Restart API on 0.0.0.0:8000 (kill anything listening there first).
#    Run from inside `api/` so pydantic-settings picks up `api/.env` and not
#    the project-root `.env` (which uses Prisma's DSN format that psycopg2
#    rejects).
echo "==> Restarting API on 0.0.0.0:8000…"
PIDS="$(lsof -tiTCP:8000 -sTCP:LISTEN 2>/dev/null || true)"
if [[ -n "${PIDS:-}" ]]; then kill ${PIDS} || true; sleep 2; fi
( cd api && nohup .venv/bin/uvicorn app.main:app \
    --reload --host 0.0.0.0 --port 8000 \
    > /tmp/musiky-api.log 2>&1 & disown )

# 6) Restart Next on 0.0.0.0:3000.
echo "==> Restarting Next on 0.0.0.0:3000…"
PIDS="$(lsof -tiTCP:3000 -sTCP:LISTEN 2>/dev/null || true)"
if [[ -n "${PIDS:-}" ]]; then kill ${PIDS} || true; sleep 2; fi
nohup npm run dev:web -- --hostname 0.0.0.0 --port 3000 \
    > /tmp/musiky-next.log 2>&1 &
disown

# 7) Wait for both servers to come up before health-checking.
echo "==> Waiting for servers…"
for i in {1..30}; do
    api_up=$(curl -s -o /dev/null -w '%{http_code}' "http://${IP}:8000/health" || echo 000)
    next_up=$(curl -s -o /dev/null -w '%{http_code}' "http://${IP}:3000/" || echo 000)
    if [[ "$api_up" == "200" && "$next_up" == "200" ]]; then
        echo "    API: 200 · Next: 200"
        break
    fi
    sleep 1
    if [[ $i -eq 30 ]]; then
        echo "    WARNING: servers didn't come up cleanly. API=$api_up Next=$next_up"
    fi
done

# 8) Rebuild + reinstall the APK if a device is connected.
export ANDROID_HOME="${ANDROID_HOME:-$HOME/Android/Sdk}"
export ANDROID_SDK_ROOT="$ANDROID_HOME"
export PATH="$PATH:$ANDROID_HOME/platform-tools"

if ! command -v adb >/dev/null 2>&1; then
    echo "==> adb not found — skipping APK rebuild/install."
    exit 0
fi

DEVICE="$(adb devices | awk 'NR>1 && $2=="device" {print $1; exit}')"
if [[ -z "${DEVICE:-}" ]]; then
    echo "==> No ADB device attached — skipping APK rebuild/install."
    echo "    Plug your phone in with USB debugging on, then re-run this script."
    exit 0
fi
echo "==> Found device: $DEVICE"

if [[ $SKIP_BUILD -eq 1 ]]; then
    echo "==> Skipping rebuild — installed APK already targets $IP."
    echo "    (Re-run with FORCE=1 if you changed icons or native config.)"
    echo
    echo "==> Launching app…"
    adb -s "$DEVICE" shell monkey -p dev.musiky.app -c android.intent.category.LAUNCHER 1 > /dev/null 2>&1
    echo
    echo "✅ Done. App is pointing at http://${IP}:3000 / API http://${IP}:8000"
    echo "   Make sure your phone is on the same network as this laptop."
    exit 0
fi

echo "==> Capacitor sync…"
npx cap sync android > /tmp/musiky-cap-sync.log 2>&1

echo "==> Gradle assembleDebug (parallel + cached)…"
( cd android && ./gradlew --quiet --build-cache --parallel assembleDebug ) || {
    echo "ERROR: gradle build failed. See android/build/reports/." >&2
    exit 1
}

APK="android/app/build/outputs/apk/debug/app-debug.apk"
echo "==> Installing $APK on $DEVICE…"
adb -s "$DEVICE" install -r -t "$APK"

echo "==> Launching app…"
adb -s "$DEVICE" shell monkey -p dev.musiky.app -c android.intent.category.LAUNCHER 1 > /dev/null 2>&1

# Record the IP we just baked into the APK so the next run can short-circuit.
echo "$IP" > "$LAST_IP_FILE"

echo
echo "✅ Done. App is pointing at http://${IP}:3000 / API http://${IP}:8000"
echo "   Make sure your phone is on the same network as this laptop."
